func TestDeployPawn(t *testing.T) {
	db, board := Construct()
	board.Create(db)

	tests := []struct {
		input1 uint
		input2 int16
		input3 int16
		err    error
	}{
		{
			input1: 1,
			input2: 2,
			input3: 3,
			err:    nil,
		},
	}
	for _, test := range tests {
		err := board.DeployPawn(db, test.input1, test.input2, test.input3)
		if test.err != err {
			t.Errorf("Error is: %v . Expected: %v", err, test.err)
		}
	}
	Destruct()
}
